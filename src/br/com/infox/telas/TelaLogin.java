    public void logar() {
        String sql = "SELECT * FROM tbusuarios WHERE login=? and senha=?";
        try {
            // as linhas abaixo preparam a consulta ao banco de dados em funcao do que foi digitado nas caixas de texto 
            // o ? eh substituido pelo conteudo das variaveis
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUser.getText()); // Indica o que foi digitado no campo 1
            pst.setString(2, txtSenha.getText()); // Indica o que foi digitado no campo 2

            // A linha abaixo executa a query -> consulta
            rs = pst.executeQuery();

            //Se existir usuario e senha correspondente
            if (rs.next()) {
                //A linha abaixo obtem o conteudo do campo perfilda tabela tbusuario
                String perfil = rs.getString(6);
                //System.out.println(perfil);

                //A linha abaixo pega o conteudo do campo usuario
                String usuario = rs.getString(2);

                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                //Tratamento do perfil do usuario
                if (perfil.equals("admin")) {
                    TelaPrincipal.menRel.setEnabled(true);
                    TelaPrincipal.menCadUser.setEnabled(true);
                    TelaPrincipal.lblUser.setForeground(Color.red);
                }
                TelaPrincipal.lblUser.setText(usuario);
                this.dispose();
                conexao.close();
            }
            else {
                JOptionPane.showMessageDialog(null, "usuario e/ou senha invalido");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

